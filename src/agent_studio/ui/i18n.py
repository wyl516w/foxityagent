def normalize_language_code(language: str | None) -> str:
    value = (language or SYSTEM_LANGUAGE).strip()
    return value or SYSTEM_LANGUAGE


def resolve_ui_language(language: str, system_language: str) -> str:
    candidate = normalize_language_code(language)
    if candidate == SYSTEM_LANGUAGE:
        candidate = normalize_language_code(system_language)
    return "zh-CN" if candidate.lower().startswith("zh") else "en-US"


def translate(
    *,
    language: str,
    system_language: str,
    key: str,
    **kwargs,
) -> str:
    resolved = resolve_ui_language(language, system_language)
    template = TRANSLATIONS.get(resolved, TRANSLATIONS["en-US"]).get(
        key,
        TRANSLATIONS["en-US"].get(key, key),
    )
    return template.format(**kwargs)
